public class ImageTag {

	public ImageTag(String officialImageName, String imageName, String tagName, String lastUpdateDate, String size) {
		this.officialImageName = officialImageName;
		this.imageName = imageName;
		this.tagName = tagName;
		this.lastUpdateDate = lastUpdateDate;
		this.size = size;
	}

	public String officialImageName;
	public String imageName;
	public String tagName;
	public String lastUpdateDate;
	public String size;
}
